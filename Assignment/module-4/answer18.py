#What happens when „1‟== 1 is executed?

'''When "1" == 1 is executed, Python will compare the string "1" with the integer 1.

In Python, this comparison will always return False, because a string and an integer are different types and are not considered equal.

The expression "1" == 1 will evaluate to False.

If you want to compare the string "1" with the integer 1, you should convert the string to an integer using the int() function, like this: int("1") == 1. This will return True, because the comparison is now between two integers.'''